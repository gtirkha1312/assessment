class Restaurant {
  final String icnoUrl;
  final String name;
  final String dishname;
  final String price;
  final String time;
  final String rating;
  final String comment;
  final bool isPromoted;

  Restaurant({
    required this.icnoUrl,
    required this.name,
    required this.dishname,
    required this.price,
    required this.time,
    required this.rating,
    required this.comment,
    required this.isPromoted,
  });
}
